/**
 * @fileoverview Component for display hint modal.
 */

import {Component, Inject, Optional} from '@angular/core';
import {NgbActiveModal} from '@ng-bootstrap/ng-bootstrap';
import {RecordedVoiceovers} from 'domain/exploration/recorded-voiceovers.model';
import {SubtitledHtml} from 'domain/exploration/subtitled-html.model';
import {StateCard} from 'domain/state_card/state-card.model';
import {AudioPlayerService} from 'services/audio-player.service';
import {AutogeneratedAudioPlayerService} from 'services/autogenerated-audio-player.service';
import {HintsAndSolutionManagerService} from '../../../services/hints-and-solution-manager.service';
import {PlayerPositionService} from '../../../services/player-position.service';
import {PlayerTranscriptService} from '../../../services/player-transcript.service';
import './display-new-hint-modal.component.css';
import {Subscription} from 'rxjs';
import {
  MAT_BOTTOM_SHEET_DATA,
  MatBottomSheetRef,
} from '@angular/material/bottom-sheet';

interface HintModalData {
  activeHintIndex: number;
}

@Component({
  selector: 'oppia-new-display-hint-modal',
  templateUrl: './display-new-hint-modal.component.html',
  styleUrls: ['./display-new-hint-modal.component.css'],
})
export class DisplayNewHintModalComponent {
  // These properties are initialized using Angular lifecycle hooks
  // and we need to do non-null assertion. For more information, see
  // https://github.com/oppia/oppia/wiki/Guide-on-defining-types#ts-7-1
  activeHintIndex!: number;
  COMPONENT_NAME_HINT!: string;
  hint!: SubtitledHtml;
  numHintsReleased = 0;
  displayedCard!: StateCard;
  recordedVoiceovers!: RecordedVoiceovers;
  hintContentId!: string;

  private subscriptions = new Subscription();

  constructor(
    // @Optional() is used because at a time only one
    // modal service will be injected based upon the
    // screen size, other will be null.
    @Optional() private ngbActiveModal: NgbActiveModal,
    @Optional() private bottomSheetRef: MatBottomSheetRef,
    @Optional() @Inject(MAT_BOTTOM_SHEET_DATA) private data: HintModalData,
    private audioPlayerService: AudioPlayerService,
    private autogeneratedAudioPlayerService: AutogeneratedAudioPlayerService,
    private hintsAndSolutionManagerService: HintsAndSolutionManagerService,
    private playerPositionService: PlayerPositionService,
    private playerTranscriptService: PlayerTranscriptService
  ) {}

  ngOnInit(): void {
    if (this.data?.activeHintIndex !== undefined) {
      this.activeHintIndex = this.data.activeHintIndex;
    }
    this.updateHintModal(this.activeHintIndex);

    this.subscriptions.add(
      this.hintsAndSolutionManagerService.numHintsReleased$.subscribe(value => {
        this.numHintsReleased = value;
      })
    );
  }

  updateHintModal(index: number): void {
    this.activeHintIndex = index;
    let displayHint = this.hintsAndSolutionManagerService.displayHint(index);
    if (displayHint === null) {
      throw new Error('Hint not found.');
    }
    this.hint = displayHint;
    this.displayedCard = this.playerTranscriptService.getCard(
      this.playerPositionService.getDisplayedCardIndex()
    );
    let contentId = this.hint.contentId;
    if (contentId === null) {
      throw new Error('Content id not found.');
    }
    this.hintContentId = contentId;

    this.audioPlayerService.onAutoplayAudio.emit();
  }

  ngOnDestroy(): void {
    this.subscriptions.unsubscribe();
  }

  closeModal(): void {
    this.audioPlayerService.stop();
    this.autogeneratedAudioPlayerService.cancel();
    if (this.bottomSheetRef) {
      this.bottomSheetRef.dismiss();
    } else if (this.ngbActiveModal) {
      this.ngbActiveModal.dismiss('cancel');
    }
  }
}
