/**
 * @fileoverview Tests for voiceover autogeneration information modal component.
 */

import {ComponentFixture, TestBed, waitForAsync} from '@angular/core/testing';
import {NgbActiveModal} from '@ng-bootstrap/ng-bootstrap';
import {AutogeneratedVoiceoverRunInfoModalComponent} from './autogenerated-voiceover-run-info-modal.component';
import {MatTabsModule} from '@angular/material/tabs';
import {CUSTOM_ELEMENTS_SCHEMA} from '@angular/core';

describe('Voiceover regeneration information modal', () => {
  let fixture: ComponentFixture<AutogeneratedVoiceoverRunInfoModalComponent>;
  let componentInstance: AutogeneratedVoiceoverRunInfoModalComponent;
  let closeSpy: jasmine.Spy;
  let ngbActiveModal: NgbActiveModal;

  beforeEach(waitForAsync(() => {
    TestBed.configureTestingModule({
      imports: [MatTabsModule],
      declarations: [AutogeneratedVoiceoverRunInfoModalComponent],
      providers: [NgbActiveModal],
      schemas: [CUSTOM_ELEMENTS_SCHEMA],
    }).compileComponents();
  }));

  beforeEach(() => {
    fixture = TestBed.createComponent(
      AutogeneratedVoiceoverRunInfoModalComponent
    );
    componentInstance = fixture.componentInstance;
    ngbActiveModal = TestBed.inject(NgbActiveModal);
    closeSpy = spyOn(ngbActiveModal, 'close').and.callThrough();
  });

  it('should create', () => {
    expect(componentInstance).toBeDefined();
  });

  it('should be able to close modal', () => {
    componentInstance.close();
    expect(closeSpy).toHaveBeenCalled();
  });

  it('should copy error to clipboard', () => {
    const error = 'Test error message';
    spyOn(navigator.clipboard, 'writeText').and.callFake(() =>
      Promise.resolve()
    );

    componentInstance.copyErrorToClipboard(error);

    expect(navigator.clipboard.writeText).toHaveBeenCalledWith(error);
  });
});
